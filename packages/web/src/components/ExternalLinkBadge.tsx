/**
 * ExternalLinkBadge - Clickable badge that links to external site
 * Shows tooltip on hover and external link icon
 */

import Tooltip from './Tooltip';

interface ExternalLinkBadgeProps {
  label: string;
  href: string;
  tooltip?: string;
}

export default function ExternalLinkBadge({ label, href, tooltip }: ExternalLinkBadgeProps) {
  const badge = (
    <a
      href={href}
      target="_blank"
      rel="noopener noreferrer"
      className="inline-flex items-center gap-1 px-1.5 py-0.5 rounded text-xs bg-blue-50 text-blue-600 hover:bg-blue-100 hover:text-blue-800 transition-colors font-mono"
    >
      {label}
      <svg className="w-3 h-3 opacity-60" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14" />
      </svg>
    </a>
  );

  if (tooltip) {
    return (
      <Tooltip content={tooltip} position="top">
        {badge}
      </Tooltip>
    );
  }

  return badge;
}
